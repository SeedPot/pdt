			<p>El almacenamiento de energía tiene un rol crítico en asegurar el futuro energético y esto se debe a que servirán como
				una “reserva de electricidad” de mucho mayor capacidad que cualquier reserva de combustibles fósiles, estabiliza la red
				de distribución y transmisión, permite un uso más eficiente de la generación existente, hace viable económicamente las
