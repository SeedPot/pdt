			<p>El participante quedará capacitado para realizar el dimensionamiento, instalación, mantención y puesta en marcha de sistemas
				solares fotovoltaicos para la generación de electricidad; identificando los procedimientos en una planificación adecuada;
				reconociendo y ubicando los distintos componentes del sistema solar; instalando de acuerdo a los procedimientos establecidos
				y cumpliendo todas las exigencias de seguridad con mención a la legislación actual que regula o beneficia este tipo de
				tecnologías.</p><br>
			<p>Este módulo estará a cargo del Centro de Capacitación de Energías Renovables, empresa privada con sede en Santiago de
				Chile, integrada por profesionales de gran experiencia y estudios formales en áreas de la ingeniería, electricidad, mecánica
				y ERNC. Se trata de una empresa con Certificación en Sistema de Gestión de la Calidad ISO 9001:2008 y la Norma Chilena
				NCh 2728:2003, registrada como OTEC y orientada en específico a temas de energías renovables.
			</p>
